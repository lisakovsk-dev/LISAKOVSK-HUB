# Функция вызова OpenRouter
async def ask_openrouter(user_message: str, history: list) -> str:
    """Отправляет запрос в OpenRouter с системным промптом."""
    messages = [{"role": "system", "content": SYSTEM_PROMPT}]
    messages.extend(history)  # история
    messages.append({"role": "user", "content": user_message})

    headers = {
        "Authorization": f"Bearer {OPENROUTER_API_KEY}",
        "Content-Type": "application/json",
        "HTTP-Referer": "https://lisakovsk-hub.onrender.com",  # Можно заменить
    }

    data = {
        "model": MODEL,
        "messages": messages,
    }

    async with httpx.AsyncClient(timeout=30.0) as client:
        response = await client.post(OPENROUTER_URL, headers=headers, json=data)
        response.raise_for_status()
        return response.json()["choices"][0]["message"]["content"]

