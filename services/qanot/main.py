# Обработка всех текстовых сообщений
@router.message()
async def handle_message(message: Message):
    user_text = message.text
    if not user_text:
        await message.answer("Пожалуйста, отправьте текстовое сообщение.")
        return

    # Отправляем индикатор набора текста
    await bot.send_chat_action(chat_id=message.chat.id, action="typing")

    try:
        # История (базовая) — можно позже добавить постоянную память
        history = []  # Здесь можно хранить последние N сообщений

        answer = await ask_openrouter(user_text, history)

        # Отправляем ответ, разбивая на части, если он длинный
        if len(answer) > 4000:
            for i in range(0, len(answer), 4000):
                await message.answer(answer[i:i+4000])
        else:
            await message.answer(answer)

    except httpx.HTTPStatusError as e:
        logging.error(f"OpenRouter API error: {e}")
        await message.answer(f"❌ Ошибка API: {e.response.status_code}. Проверь ключ OpenRouter.")
    except Exception as e:
        logging.error(f"Unknown error: {e}")
        await message.answer(f"❌ Произошла неизвестная ошибка. Посмотри логи.")

